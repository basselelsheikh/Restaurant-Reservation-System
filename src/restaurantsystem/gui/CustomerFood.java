    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:

        int size = c.getOrders().size();
        for (int i = 0; i < size; i++) {

            Total += (c.getOrders().get(i).getPrice_per_dish()) * (c.getOrders().get(i).getQuantity());

        }
        c.setTotal(Total);
        if (Total == 0) {

            int result = JOptionPane.showConfirmDialog(this, "Your Cart is Empty, Save Reservation anyways?", "Empty cart", JOptionPane.YES_NO_OPTION);
            if (result == 0) {
                JOptionPane.showMessageDialog(this, "Reservation saved successfully");
                try {
                    customer_list.getListcustomer().add(c);
                    customer_list.marshal();
                } catch (JAXBException | FileNotFoundException ex) {
                    Logger.getLogger(CustomerFood.class.getName()).log(Level.SEVERE, null, ex);
                }
                this.dispose();
                return;

            } else {
                return;
            }

        }

        jLabel7.setText("Total: $" + Total);
        try {
            customer_list.getListcustomer().add(c);
            customer_list.marshal();
        } catch (JAXBException | FileNotFoundException ex) {
            Logger.getLogger(CustomerFood.class.getName()).log(Level.SEVERE, null, ex);
        }
        JOptionPane.showMessageDialog(this, "Enjoy your meal ;)");

    }//GEN-LAST:event_jButton4ActionPerformed

