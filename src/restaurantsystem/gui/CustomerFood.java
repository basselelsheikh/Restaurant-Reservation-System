    public void populateComboBox() {
        for (int i = 0; i < res.getDishes().getListdish().size(); i++) {
            switch (res.getDishes().getListdish().get(i).getType()) {
                case "main_course":
                    dish2 = new MainCourse(res.getDishes().getListdish().get(i).getName(), res.getDishes().getListdish().get(i).getPrice());
                    mains.add(dish2);
                    break;
                case "appetizer":
                    dish1 = new Appetizer(res.getDishes().getListdish().get(i).getName(), res.getDishes().getListdish().get(i).getPrice());
                    appetiz.add(dish1);
                    break;
                case "desert":
                    dish3 = new Dessert(res.getDishes().getListdish().get(i).getName(), res.getDishes().getListdish().get(i).getPrice());
                    des.add(dish3);
                    break;
                default:
                    break;
            }

        }

        String[] mainlist = new String[mains.size()];
        String[] appetizerlist = new String[appetiz.size()];
        String[] dessertlist = new String[des.size()];

        for (int i = 0; i < mains.size(); i++) {
            mainlist[i] = mains.get(i).getName() + ", Price: $" + mains.get(i).getPrice() + " tax: 15%";
            jComboBox3.setModel(new javax.swing.DefaultComboBoxModel<>(mainlist));
        }
        for (int i = 0; i < appetiz.size(); i++) {
            appetizerlist[i] = appetiz.get(i).getName() + ", Price: $" + appetiz.get(i).getPrice() + " tax: 10%";
            jComboBox1.setModel(new javax.swing.DefaultComboBoxModel<>(appetizerlist));
        }
        for (int i = 0; i < des.size(); i++) {
            dessertlist[i] = des.get(i).getName() + ", Price: $" + des.get(i).getPrice() + " tax: 20%";
            jComboBox2.setModel(new javax.swing.DefaultComboBoxModel<>(dessertlist));
        }

    }

