    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        String dish_name = jTextField1.getText().trim();
        for (int i = 0; i < restaurant_runtime.getDishes().getListdish().size(); i++) {
            if (dish_name.equalsIgnoreCase(restaurant_runtime.getDishes().getListdish().get(i).getName())) {
                JOptionPane.showMessageDialog(this, "This Dish Already Exists");
                return;
            }
        }

        if (dish_name.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Enter a dish name");
            return;
        }
        try {
            price = Integer.parseInt(jTextField3.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Enter a valid price");
            return;
        }
        int type = jComboBox4.getSelectedIndex();
        switch (type) {
            case 0:
                dish = new DishXml(dish_name, price, "main_course");
                break;
            case 1:
                dish = new DishXml(dish_name, price, "appetizer");
                break;
            case 2:
                dish = new DishXml(dish_name, price, "desert");
                break;
            default:
                break;
        }
        try {
            restaurant_runtime.getDishes().getListdish().add(dish);
         restaurant_runtime.marshal("programdata.xml");
            update_data();
            JOptionPane.showMessageDialog(this, "Dish Added Successfully");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Cannot Save Dish");
        }


    }//GEN-LAST:event_jButton6ActionPerformed

