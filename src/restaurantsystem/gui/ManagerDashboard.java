    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        int index = jComboBox1.getSelectedIndex();
        if (customers.getListcustomer().isEmpty()) {
            JOptionPane.showMessageDialog(this, "oops,No one came in today");
        } else {
            jTextArea1.setText(null);
            if (customers.getListcustomer().get(index).getTable() instanceof SmokingTable) {
                st = (SmokingTable) customers.getListcustomer().get(index).getTable();
                jTextArea1.append("Table ID: " + st.getId() + "\n");
                jTextArea1.append("number of seats:  " + st.getNo_of_seats() + "\n");
                jTextArea1.append("Location: Smoking Area \n");

            } else {
                nst = (NonSmokingTable) customers.getListcustomer().get(index).getTable();
                jTextArea1.append("Table ID: " + nst.getId() + "\n");
                jTextArea1.append("number of seats:  " + nst.getNo_of_seats() + "\n");
                jTextArea1.append("Location: Non-Smoking Area \n");
            }

            jTextArea1.append("Orders: \n");
            for (int i = 0; i < customers.getListcustomer().get(index).getOrders().size(); i++) {
                jTextArea1.append(customers.getListcustomer().get(index).getOrders().get(i).toString() + "\n");
            }
            jTextArea1.append("Total Paid: " + Double.toString(customers.getListcustomer().get(index).getTotal()));
        }

    }//GEN-LAST:event_jButton1ActionPerformed

