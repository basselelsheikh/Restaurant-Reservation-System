    public void update_data() {
        try {
            restaurant_runtime = new Restaurant().unMarshal("programdata.xml");
            customers = new ListCustomerData().unMarshal();
        } catch (FileNotFoundException ex) {
            JOptionPane.showMessageDialog(this, "Cannot Load data");
        }

        employees = new String[restaurant_runtime.getUsers().getListuser().size()];
        for (int i = 0; i < restaurant_runtime.getUsers().getListuser().size(); i++) {
            employees[i] = restaurant_runtime.getUsers().getListuser().get(i).getName();

        }
        jComboBox2.setModel(new javax.swing.DefaultComboBoxModel<>(employees));
        jTextArea2.setText(null);

        jTextArea3.setText(null);
        for (int i = 0; i < restaurant_initial.getTables().getListtable().size(); i++) {
            jTextArea2.append("Table ID: " + restaurant_initial.getTables().getListtable().get(i).getNumber() + "\n" + "Number of seats: " + restaurant_initial.getTables().getListtable().get(i).getNo_of_seats() + "\n");

            if (restaurant_initial.getTables().getListtable().get(i).isSmoking()) {
                jTextArea2.append("Location: Smoking Area\n\n");
            } else {
                jTextArea2.append("Location: Non-Smoking Area\n\n");

            }
        }
        jTextArea3.append("Tax per dish: \n Main Courses: 15% \n Appetizer: 10% \n Dessert: 20%\n\n");
        for (int i = 0; i < restaurant_runtime.getDishes().getListdish().size(); i++) {
            jTextArea3.append("Dish Name: " + restaurant_runtime.getDishes().getListdish().get(i).getName() + "\n" + "Price before tax: " + restaurant_runtime.getDishes().getListdish().get(i).getPrice() + "\n");
            if (restaurant_runtime.getDishes().getListdish().get(i).getType().equalsIgnoreCase("main_course")) {
                jTextArea3.append("Type: Main Course\n\n");
            } else if (restaurant_runtime.getDishes().getListdish().get(i).getType().equalsIgnoreCase("appetizer")) {
                jTextArea3.append("Type: Appetizer\n\n");

            } else if (restaurant_runtime.getDishes().getListdish().get(i).getType().equalsIgnoreCase("desert")) {
                jTextArea3.append("Type: Dessert\n\n");

            }

        }

    }

